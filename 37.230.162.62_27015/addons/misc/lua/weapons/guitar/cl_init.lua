local color_black = Color(15, 15, 15)
local function drawShadowText(text, font, x, y, color, align1, align2)
	local w, h = draw.SimpleText(text, font, x, y, color_black, align1, align2)
	draw.SimpleText(text, font, x - 1, y - 1, color, align1, align2)
	return w, h
end

local ss = util.ScreenScale

local stencil = "[ %s ] - %s"
local stepW, stepH, stepY = ss(15), ss(25), ss(10)
function SWEP:DrawHUD()
	local song = self.Songs[self:GetSong()]
	if not song then return end
	local song_name = song.name or "Неизвестная песня"

	local w = ScrW() - stepW
	local h = ScrH() - stepH
	local _, y = drawShadowText(stencil:format(input.LookupBinding("attack"):upper(), song_name), "moneyCheckerFont", w, h, color_white, TEXT_ALIGN_RIGHT, TEXT_ALIGN_CENTER)
	h = h - y - stepY

	h = h - stepY * 2
	local text = stencil:format(input.LookupBinding("reload"):upper(), "сменить песню")
	drawShadowText(text, "Trebuchet24", w, h, color_white, TEXT_ALIGN_RIGHT, TEXT_ALIGN_CENTER)
end

local moneyCheckScroll
function SWEP:Reload()
	if not IsFirstTimePredicted() then return true end

	if (self.ReloadDelay or 0) > CurTime() then return true end
	self.ReloadDelay = CurTime() + 0.3

	if moneyCheckScroll then
		moneyCheckScroll:Remove()
		moneyCheckScroll = nil
		gui.EnableScreenClicker(false)
		return true
	end

	local scrollW, scrollH = ss(400), ss(350)
	local w, h = ScrW() * 0.1, ScrH()
	moneyCheckScroll = vgui.Create("DScrollPanel")
	moneyCheckScroll:SetSize(scrollW, scrollH)
	moneyCheckScroll:SetPos(w, h / 1.5 - (scrollH * 0.5))

	gui.EnableScreenClicker(true)
	input.SetCursorPos(w, h / 1.5)

	for k, v in ipairs(self.Songs) do
		local button = moneyCheckScroll:Add("DButton")
		button:SetText(v.name)
		button:DockMargin(0, 0, 5, 5)
		button:Dock(TOP)

		button.DoClick = function()
			surface.PlaySound("UI/buttonclick.wav")

			if IsValid(moneyCheckScroll) then
				moneyCheckScroll:Remove()
				moneyCheckScroll = nil
			end

			gui.EnableScreenClicker(false)
			netstream.Start("guitar_SetSong", k)
		end
	end

	return true
end

function SWEP:Initialize()
	self.VElements = table.FullCopy( self.VElements )
	self.WElements = table.FullCopy( self.WElements )
	self.ViewModelBoneMods = table.FullCopy( self.ViewModelBoneMods )

	self:CreateModels(self.VElements) // create viewmodels
	self:CreateModels(self.WElements) // create worldmodels
	
	if IsValid(self:GetOwner()) then
		local vm = self:GetOwner():GetViewModel()
		if IsValid(vm) then
			self:ResetBonePositions(vm)
			
			if (self.ShowViewModel == nil or self.ShowViewModel) then
				vm:SetColor(Color(255,255,255,255))
			else
				vm:SetColor(Color(255,255,255,1))
				vm:SetMaterial("Debug/hsv")			
			end
		end
	end
end

local function RemoveClientModels(tab)
  if not istable(tab) then return end
  for k, v in pairs(tab) do
    local ent = v.modelEnt
    if IsValid(ent) then
      ent:Remove()
    end
  end
end
function SWEP:OnRemove()
	self:Holster()

  if CLIENT then
    RemoveClientModels(self.VElements)
    RemoveClientModels(self.WElements)
  end
end

